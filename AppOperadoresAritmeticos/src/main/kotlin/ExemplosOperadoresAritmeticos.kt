fun main() {

    var a = 10
    var b = 5

    var soma = a.plus(b)
    var subtracao = a.minus(b)
    var multiplicacao = a.times(b)
    var divisao = a.div(b)
    var modulo = a.mod(b)

    println("Soma: $soma")
    println("Subtração: $subtracao")
    println("Multiplicação: $multiplicacao")
    println("Divisão: $divisao")
    println("Modulo: $modulo")

}