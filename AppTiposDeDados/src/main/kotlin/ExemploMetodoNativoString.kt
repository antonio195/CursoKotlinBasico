fun main() {

    val name = "antonio Costa dos Santos"

    println("Lengh: ${name.length}")
    println("Capitalize: ${name.capitalize()}")
    println("Lower case: ${name.lowercase()}")
    println("Upper case: ${name.toUpperCase()}")
    println("Is empty: ${name.isEmpty()}")
    println("Last: ${name.last()}")
    println("Reversed: ${name.reversed()}")

}